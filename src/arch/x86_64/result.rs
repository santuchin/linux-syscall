
use crate::types::c_long;

#[derive(Debug, Clone, Copy)]
#[repr(transparent)]
pub struct Result {
	raw: c_long,
}


impl Result {

	pub fn catch(self) -> core::result::Result<c_long, Error> {

		match self.raw {
			-0xfff..0 => Err(unsafe { std::mem::transmute((-(self.raw as i16)) as u16) }),
			_ => Ok(self.raw),
		}
	}

	pub fn catch_unchecked(self) -> c_long {
		self.raw
	}

}

impl From<c_long> for Result {

    fn from(raw: c_long) -> Self {
    	Self { raw }
    }
}


